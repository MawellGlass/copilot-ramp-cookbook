/* ──────────────────────────────────────────────────────────────────────────
 * role-path.js — interactive "proposed path" engine for the persona pages (v2).
 *
 * Each persona page (docs/roles/*.md) drops in:
 *   <div id="role-path"></div>
 *   <script id="role-path-config" type="application/json"> { ...config... } </script>
 *
 * v2 capabilities:
 *   • Variable controls are ADDITIVE — raising comfort / pace / goal or picking a
 *     function reveals MORE steps (not just fewer). Newly revealed steps carry an
 *     "Added for you" badge.
 *   • Steps are ordered by stage, then authored order, then goal weighting.
 *   • Readers can remove any step (×) and add more from a catalog-driven picker
 *     ("+ Add to my path") with Stage / Function / Level filters + search. The
 *     picker is scoped to the persona's roles by default, with a "Show
 *     everything" toggle.
 *   • Manual add/remove choices persist per persona in localStorage, with a
 *     "Reset path" control.
 *
 * Catalog (docs/path-catalog.json) is generated by tooling/gen_path_catalog.py.
 * ────────────────────────────────────────────────────────────────────────── */
(function () {
  "use strict";

  // ── Variable catalogue (labels + ordered options) ─────────────────────────
  var VARS = {
    dept: {
      label: "Your function",
      opts: [
        { v: "general", t: "General" },
        { v: "hr", t: "HR" },
        { v: "it", t: "IT" },
        { v: "finance", t: "Finance" },
        { v: "sales", t: "Sales" },
        { v: "legal", t: "Legal" }
      ]
    },
    pace: {
      label: "Your pace",
      opts: [
        { v: "exploring", t: "Just exploring" },
        { v: "steady", t: "Steady" },
        { v: "ramp", t: "Multi-week ramp" }
      ]
    },
    skill: {
      label: "Your comfort",
      opts: [
        { v: "new", t: "Brand new" },
        { v: "some", t: "Some experience" },
        { v: "experienced", t: "Experienced" }
      ]
    },
    goal: {
      label: "Your goal",
      opts: [
        { v: "personal", t: "Personal productivity" },
        { v: "team", t: "Team rollout" },
        { v: "build", t: "Build an agent" }
      ]
    }
  };

  // Stage ordering for the timeline (lower = earlier).
  var STAGE_ORDER = {
    prereqs: 0, empowerment: 0,
    chat: 1, "first-party": 2, cowork: 3,
    "agent-builder": 4, studio: 5, foundry: 6,
    governance: 7
  };

  // Function filter labels for the picker.
  var FN_LABELS = { hr: "HR", it: "IT", finance: "Finance", sales: "Sales", legal: "Legal" };

  // Level ordering / labels for the picker.
  var LEVELS = [
    { v: "starter", t: "Starter" },
    { v: "intermediate", t: "Intermediate" },
    { v: "advanced", t: "Advanced" }
  ];

  function esc(s) {
    return String(s == null ? "" : s)
      .replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;")
      .replace(/"/g, "&quot;");
  }

  function stageRank(key) {
    return STAGE_ORDER[key] != null ? STAGE_ORDER[key] : 50;
  }

  // A step passes a list-filter when the step omits the key (applies to all) or
  // the selected value is in the step's allow-list. "all" in a list = wildcard.
  function passes(stepList, value) {
    if (!stepList || !stepList.length) return true;
    return stepList.indexOf("all") !== -1 || stepList.indexOf(value) !== -1;
  }

  // Resolve an authored step against a selection: dept variant merge + visibility.
  function resolveStep(step, sel) {
    if (!passes(step.dept, sel.dept)) return null;
    if (!passes(step.skill, sel.skill)) return null;
    if (!passes(step.goal, sel.goal)) return null;
    if (!passes(step.pace, sel.pace)) return null;

    var out = {
      id: step._id,
      stage: step.stage,
      stageKey: step.stageKey || "",
      title: step.title,
      href: step.href,
      why: step.why,
      optional: !!step.optional,
      external: !!step.external,
      _i: step._i,
      _bump: 0,
      _goalMatch: false,
      _deptMatch: false,
      _manual: false
    };

    if (step.deptVariants && step.deptVariants[sel.dept]) {
      var dv = step.deptVariants[sel.dept];
      if (dv.title) out.title = dv.title;
      if (dv.href) out.href = dv.href;
      if (dv.why) out.why = dv.why;
      out._deptMatch = true;
    }

    if (step.weightGoal && typeof step.weightGoal[sel.goal] === "number") {
      out._bump = step.weightGoal[sel.goal];
      if (out._bump > 0) out._goalMatch = true;
    }

    return out;
  }

  // All authored steps that resolve under a given selection (set of ids).
  function resolvedIds(cfg, sel) {
    var set = {};
    (cfg.steps || []).forEach(function (step) {
      if (resolveStep(step, sel)) set[step._id] = true;
    });
    return set;
  }

  function sortSteps(list) {
    list.sort(function (a, b) {
      var sr = stageRank(a.stageKey) - stageRank(b.stageKey);
      if (sr !== 0) return sr;
      return (a._i - a._bump * 0.5) - (b._i - b._bump * 0.5);
    });
  }

  // ── Controls + summary ────────────────────────────────────────────────────
  function buildControls(cfg, sel) {
    var vars = cfg.variables || [];
    if (!vars.length) return "";
    var groups = vars.map(function (key) {
      var def = VARS[key];
      if (!def) return "";
      var btns = def.opts.map(function (o) {
        var active = sel[key] === o.v ? " active" : "";
        return '<button type="button" class="rp-opt' + active +
          '" data-var="' + key + '" data-val="' + esc(o.v) + '">' + esc(o.t) + "</button>";
      }).join("");
      return '<div class="rp-control">' +
        '<span class="rp-control-label">' + esc(def.label) + "</span>" +
        '<div class="rp-seg">' + btns + "</div></div>";
    }).join("");
    return '<div class="rp-controls" role="group" aria-label="Customise your path">' + groups + "</div>";
  }

  function buildSummary(cfg, sel, count) {
    var bits = (cfg.variables || []).map(function (key) {
      var def = VARS[key];
      if (!def) return null;
      var opt = def.opts.filter(function (o) { return o.v === sel[key]; })[0];
      return opt ? esc(opt.t) : null;
    }).filter(Boolean);
    var tuned = bits.length ? " tuned for: " + bits.join(" &middot; ") : "";
    return '<p class="rp-summary"><strong>' + count + " step" + (count === 1 ? "" : "s") +
      "</strong>" + tuned + "</p>";
  }

  function buildTimeline(steps) {
    if (!steps.length) {
      return '<p class="rp-empty">No steps match that combination yet — try a different mix or add some below.</p>';
    }
    var items = steps.map(function (s, idx) {
      var ext = s.external ? ' target="_blank" rel="noopener"' : "";
      var tags = "";
      if (s._added) tags += '<span class="rp-tag rp-tag-added">Added for you</span>';
      if (s._manual) tags += '<span class="rp-tag rp-tag-manual">You added this</span>';
      if (s._goalMatch) tags += '<span class="rp-tag rp-tag-goal">Matches your goal</span>';
      if (s._deptMatch) tags += '<span class="rp-tag rp-tag-dept">Tailored example</span>';
      if (s.optional) tags += '<span class="rp-tag rp-tag-opt">Optional</span>';
      return '<li class="rp-step' + (s.optional ? " is-optional" : "") + '">' +
        '<span class="rp-num">' + (idx + 1) + "</span>" +
        '<div class="rp-body">' +
          '<span class="rp-stage">' + esc(s.stage) + "</span>" +
          '<a class="rp-title" href="' + esc(s.href) + '"' + ext + ">" + esc(s.title) + " &rarr;</a>" +
          (s.why ? '<p class="rp-why">' + esc(s.why) + "</p>" : "") +
          (tags ? '<div class="rp-tags">' + tags + "</div>" : "") +
        "</div>" +
        '<button type="button" class="rp-remove" data-id="' + esc(s.id) +
          '" data-manual="' + (s._manual ? "1" : "0") + '" title="Remove from my path" aria-label="Remove ' +
          esc(s.title) + ' from my path">&times;</button>' +
      "</li>";
    }).join("");
    return '<ol class="rp-timeline">' + items + "</ol>";
  }

  // ── Picker ────────────────────────────────────────────────────────────────
  function catalogPool(ctx) {
    // Internal items + external, optionally scoped to the persona's roles.
    var all = (ctx.catalog.items || []).concat(ctx.catalog.external || []);
    if (ctx.pf.everything) return all;
    var roles = ctx.cfg.roleKeys || [];
    if (!roles.length) return all;
    return all.filter(function (it) {
      var r = it.roles || [];
      if (!r.length) return false;
      return r.some(function (x) { return roles.indexOf(x) !== -1; });
    });
  }

  function itemId(it) { return it.path || it.href; }

  function itemHref(it) {
    return it.path ? "../../" + it.path : it.href;
  }

  function buildPicker(ctx) {
    var pf = ctx.pf;
    var pool = catalogPool(ctx);

    // Stages present in the pool, ordered.
    var stageKeys = [];
    pool.forEach(function (it) {
      if (it.stage && stageKeys.indexOf(it.stage) === -1) stageKeys.push(it.stage);
    });
    stageKeys.sort(function (a, b) { return stageRank(a) - stageRank(b); });

    function seg(varName, current, opts) {
      var btns = opts.map(function (o) {
        var active = current === o.v ? " active" : "";
        return '<button type="button" class="rp-opt rp-pick-filter' + active +
          '" data-pf="' + varName + '" data-val="' + esc(o.v) + '">' + esc(o.t) + "</button>";
      }).join("");
      return '<div class="rp-seg">' + btns + "</div>";
    }

    var stageOpts = [{ v: "all", t: "All stages" }].concat(stageKeys.map(function (k) {
      return { v: k, t: (k.charAt(0).toUpperCase() + k.slice(1)).replace("-", " ") };
    }));
    var fnOpts = [{ v: "all", t: "All" }].concat(
      Object.keys(FN_LABELS).map(function (k) { return { v: k, t: FN_LABELS[k] }; }));
    var levelOpts = [{ v: "all", t: "All levels" }].concat(LEVELS);

    var filters =
      '<div class="rp-pick-filters">' +
        '<div class="rp-control"><span class="rp-control-label">Stage</span>' + seg("stage", pf.stage, stageOpts) + "</div>" +
        '<div class="rp-control"><span class="rp-control-label">Function</span>' + seg("fn", pf.fn, fnOpts) + "</div>" +
        '<div class="rp-control"><span class="rp-control-label">Level</span>' + seg("level", pf.level, levelOpts) + "</div>" +
      "</div>" +
      '<div class="rp-pick-tools">' +
        '<input type="search" class="rp-pick-search" placeholder="Search the catalog…" value="' + esc(pf.q) + '" aria-label="Search the catalog">' +
        '<label class="rp-pick-everything"><input type="checkbox" class="rp-pick-all"' +
          (pf.everything ? " checked" : "") + "> Show everything</label>" +
      "</div>";

    return '<div class="rp-pick-panel">' + filters +
      '<div class="rp-pick-list">' + buildPickList(ctx, pool) + "</div></div>";
  }

  function buildPickList(ctx, pool) {
    var pf = ctx.pf;
    var inPath = ctx.inPath; // map of ids already on the path
    var q = (pf.q || "").trim().toLowerCase();

    var rows = pool.filter(function (it) {
      if (inPath[itemId(it)]) return false;
      if (pf.stage !== "all" && it.stage !== pf.stage) return false;
      if (pf.level !== "all" && (it.level || "") !== pf.level) return false;
      if (pf.fn !== "all" && (it.fn || []).indexOf(pf.fn) === -1) return false;
      if (q && (it.title || "").toLowerCase().indexOf(q) === -1) return false;
      return true;
    });

    rows.sort(function (a, b) {
      var sr = stageRank(a.stage) - stageRank(b.stage);
      if (sr !== 0) return sr;
      return (a.title || "").localeCompare(b.title || "");
    });

    if (!rows.length) {
      return '<p class="rp-empty">Nothing matches those filters. Try “Show everything” or clear the search.</p>';
    }

    return rows.slice(0, 60).map(function (it) {
      var meta = [it.stageLabel || it.stage, it.level, it.kind === "external" ? "external" : null]
        .filter(Boolean).map(esc).join(" &middot; ");
      return '<div class="rp-pick-item">' +
        '<div class="rp-pick-info"><span class="rp-pick-title">' + esc(it.title) + "</span>" +
          '<span class="rp-pick-meta">' + meta + "</span></div>" +
        '<button type="button" class="rp-pick-add" data-add="' + esc(itemId(it)) + '">Add</button>' +
      "</div>";
    }).join("");
  }

  // ── Full render ───────────────────────────────────────────────────────────
  function render(ctx) {
    var cfg = ctx.cfg, sel = ctx.sel;

    // Baseline (default selection) ids — anything beyond is "Added for you".
    var baseIds = resolvedIds(cfg, ctx.defaults);

    var steps = [];
    (cfg.steps || []).forEach(function (step) {
      if (ctx.persist.removed.indexOf(step._id) !== -1) return; // user removed
      var r = resolveStep(step, sel);
      if (!r) return;
      r._added = !baseIds[step._id];
      steps.push(r);
    });

    // Manually added catalog items.
    var inPath = {};
    steps.forEach(function (s) { if (s.href) inPath[s.href] = true; });
    ctx.persist.added.forEach(function (id, idx) {
      var it = ctx.byId[id];
      if (!it) return;
      var href = itemHref(it);
      steps.push({
        id: id,
        stage: it.stageLabel || it.stage,
        stageKey: it.stage,
        title: it.title,
        href: href,
        why: "",
        optional: false,
        external: it.kind === "external",
        _i: 2000 + idx,
        _bump: 0,
        _goalMatch: false,
        _deptMatch: false,
        _added: false,
        _manual: true
      });
      inPath[href] = true;
      inPath[id] = true;
    });
    // Authored hrefs/paths also count as "in path" for the picker.
    (cfg.steps || []).forEach(function (step) {
      if (step.href) inPath[step.href] = true;
    });
    ctx.inPath = inPath;

    sortSteps(steps);

    var hasChanges = ctx.persist.added.length || ctx.persist.removed.length;
    var actions =
      '<div class="rp-actions">' +
        '<button type="button" class="rp-add-toggle"' + (ctx.pickerOpen ? " aria-expanded=\"true\"" : "") + ">" +
          (ctx.pickerOpen ? "Close picker" : "+ Add to my path") + "</button>" +
        (hasChanges ? '<button type="button" class="rp-reset">Reset path</button>' : "") +
      "</div>";

    ctx.mount.innerHTML =
      buildControls(cfg, sel) +
      buildSummary(cfg, sel, steps.length) +
      buildTimeline(steps) +
      actions +
      (ctx.pickerOpen ? buildPicker(ctx) : "");

    wire(ctx);
  }

  function save(ctx) {
    try {
      localStorage.setItem("rp:" + ctx.cfg.key,
        JSON.stringify({ added: ctx.persist.added, removed: ctx.persist.removed }));
    } catch (e) { /* ignore quota / privacy mode */ }
  }

  function wire(ctx) {
    var mount = ctx.mount;

    // Variable controls.
    mount.querySelectorAll(".rp-opt:not(.rp-pick-filter)").forEach(function (btn) {
      btn.addEventListener("click", function () {
        var k = btn.getAttribute("data-var");
        var v = btn.getAttribute("data-val");
        if (!k || ctx.sel[k] === v) return;
        ctx.sel[k] = v;
        render(ctx);
      });
    });

    // Remove a step.
    mount.querySelectorAll(".rp-remove").forEach(function (btn) {
      btn.addEventListener("click", function () {
        var id = btn.getAttribute("data-id");
        var manual = btn.getAttribute("data-manual") === "1";
        if (manual) {
          var i = ctx.persist.added.indexOf(id);
          if (i !== -1) ctx.persist.added.splice(i, 1);
        } else if (ctx.persist.removed.indexOf(id) === -1) {
          ctx.persist.removed.push(id);
        }
        save(ctx);
        render(ctx);
      });
    });

    // Toggle picker.
    var toggle = mount.querySelector(".rp-add-toggle");
    if (toggle) toggle.addEventListener("click", function () {
      ctx.pickerOpen = !ctx.pickerOpen;
      render(ctx);
    });

    // Reset.
    var reset = mount.querySelector(".rp-reset");
    if (reset) reset.addEventListener("click", function () {
      ctx.persist.added = [];
      ctx.persist.removed = [];
      save(ctx);
      render(ctx);
    });

    wirePicker(ctx);
  }

  function wirePicker(ctx) {
    var mount = ctx.mount;
    var panel = mount.querySelector(".rp-pick-panel");
    if (!panel) return;

    // Filter segments.
    panel.querySelectorAll(".rp-pick-filter").forEach(function (btn) {
      btn.addEventListener("click", function () {
        var k = btn.getAttribute("data-pf");
        var v = btn.getAttribute("data-val");
        if (!k || ctx.pf[k] === v) return;
        ctx.pf[k] = v;
        refreshPicker(ctx);
      });
    });

    // Show-everything toggle.
    var all = panel.querySelector(".rp-pick-all");
    if (all) all.addEventListener("change", function () {
      ctx.pf.everything = all.checked;
      refreshPicker(ctx);
    });

    // Search (live, focus-preserving).
    var search = panel.querySelector(".rp-pick-search");
    if (search) search.addEventListener("input", function () {
      ctx.pf.q = search.value;
      var listEl = panel.querySelector(".rp-pick-list");
      if (listEl) {
        listEl.innerHTML = buildPickList(ctx, catalogPool(ctx));
        wireAdd(ctx, listEl);
      }
    });

    wireAdd(ctx, panel);
  }

  function wireAdd(ctx, root) {
    root.querySelectorAll(".rp-pick-add").forEach(function (btn) {
      btn.addEventListener("click", function () {
        var id = btn.getAttribute("data-add");
        if (id && ctx.persist.added.indexOf(id) === -1) {
          ctx.persist.added.push(id);
          // If it was previously removed as an authored step, clear that.
          var ri = ctx.persist.removed.indexOf(id);
          if (ri !== -1) ctx.persist.removed.splice(ri, 1);
          save(ctx);
        }
        render(ctx);
      });
    });
  }

  // Re-render just the picker panel (keeps timeline untouched).
  function refreshPicker(ctx) {
    var existing = ctx.mount.querySelector(".rp-pick-panel");
    if (!existing) { render(ctx); return; }
    var wrap = document.createElement("div");
    wrap.innerHTML = buildPicker(ctx);
    existing.replaceWith(wrap.firstChild);
    wirePicker(ctx);
  }

  // ── Init ──────────────────────────────────────────────────────────────────
  function loadPersist(key) {
    var out = { added: [], removed: [] };
    try {
      var raw = localStorage.getItem("rp:" + key);
      if (raw) {
        var p = JSON.parse(raw);
        if (Array.isArray(p.added)) out.added = p.added;
        if (Array.isArray(p.removed)) out.removed = p.removed;
      }
    } catch (e) { /* ignore */ }
    return out;
  }

  function init() {
    var mount = document.getElementById("role-path");
    var cfgEl = document.getElementById("role-path-config");
    if (!mount || !cfgEl) return;

    var cfg;
    try {
      cfg = JSON.parse(cfgEl.textContent);
    } catch (e) {
      mount.innerHTML = '<p class="rp-empty">Could not load this path.</p>';
      return;
    }
    if (!cfg.key) cfg.key = (document.body.getAttribute("data-rp-key") || "role");

    (cfg.steps || []).forEach(function (s, i) { s._i = i; s._id = "s" + i; });

    var def = cfg.defaults || {};
    var sel = {};
    var defaults = {};
    (cfg.variables || []).forEach(function (key) {
      var first = (VARS[key] && VARS[key].opts[0] && VARS[key].opts[0].v) || null;
      sel[key] = def[key] || first;
      defaults[key] = def[key] || first;
    });

    var ctx = {
      mount: mount,
      cfg: cfg,
      sel: sel,
      defaults: defaults,
      persist: loadPersist(cfg.key),
      catalog: { items: [], external: [] },
      byId: {},
      pickerOpen: false,
      pf: { stage: "all", fn: "all", level: "all", q: "", everything: false },
      inPath: {}
    };

    // First paint without the catalog (picker still works once it loads).
    render(ctx);

    fetch("../../path-catalog.json", { credentials: "same-origin" })
      .then(function (r) { return r.ok ? r.json() : null; })
      .then(function (cat) {
        if (!cat) return;
        ctx.catalog = cat;
        (cat.items || []).concat(cat.external || []).forEach(function (it) {
          ctx.byId[it.path || it.href] = it;
        });
        render(ctx);
      })
      .catch(function () { /* picker simply stays empty */ });
  }

  // Material instant-loading safe init.
  if (window.document$ && typeof window.document$.subscribe === "function") {
    window.document$.subscribe(init);
  } else if (document.readyState !== "loading") {
    init();
  } else {
    document.addEventListener("DOMContentLoaded", init);
  }
})();
